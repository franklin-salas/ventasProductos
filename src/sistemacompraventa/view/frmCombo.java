    private void llenarCombo() {
        List<ItemComponent> productos = nProducto.indexALL();
        for (ItemComponent producto : productos) {
            jComboItem.addItem(producto);
            
        }
    }

    public void LLenartabla(List<ItemComponent> list) {
        this.itemlist.addAll(list);
        jtableitem.showTable(itemlist);
    }
